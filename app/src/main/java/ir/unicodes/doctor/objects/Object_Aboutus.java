package ir.unicodes.doctor.objects;

public class Object_Aboutus {

    public int url;
    public String title;

    public Object_Aboutus(int url, String title) {
        this.url = url;
        this.title = title;
    }

    public int getUrl() {
        return url;
    }

    public void setUrl(int url) {
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
